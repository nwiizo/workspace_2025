use proconio::input;

fn main() {
    input! {
        s1: String,  // 高橋君の状態
        s2: String   // 青木君の状態
    }

    // as_str()を使わずに直接Stringで比較
    match (s1.as_str(), s2.as_str()) {
        ("sick", "fine") => {
            // 高橋君は具合が悪く(1,2を食べた)、青木君は大丈夫(1,3を食べた)
            // 1は青木君も食べているが大丈夫なので、2が原因
            println!("2");
        }
        ("fine", "sick") => {
            // 高橋君は大丈夫(1,2を食べた)、青木君は具合が悪い(1,3を食べた)
            // 1は高橋君も食べているが大丈夫なので、3が原因
            println!("3");
        }
        ("fine", "fine") => {
            // 両者とも大丈夫
            // 1,2,3は全て安全なので、4が原因
            println!("4");
        }
        _ => {
            // 両者とも具合が悪い場合("sick", "sick")
            // 共通して食べている1が原因
            println!("1");
        }
    }
}
